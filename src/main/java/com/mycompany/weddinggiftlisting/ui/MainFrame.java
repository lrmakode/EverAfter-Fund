    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        String name = nameField.getText();
        String number = numberField.getText();
        String village = villageField.getText();
        String amountText = amountField.getText();
        String mode = jRadioButton1.isSelected() ? "0" : "1";
        if (name.isEmpty() || number.isEmpty() || village.isEmpty() || amountText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields are required!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            double amount = Double.parseDouble(amountText);
            try (Connection conn = DriverManager.getConnection("jdbc:sqlite:data.db");
                 PreparedStatement stmt = conn.prepareStatement("INSERT INTO entries (name, number, village, mode, amount) VALUES (?, ?, ?, ?, ?)");) {
                stmt.setString(1, name);
                stmt.setString(2, number);
                stmt.setString(3, village);
                stmt.setString(4, mode);
                stmt.setDouble(5, amount);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Entry saved successfully!");
                nameValueLable.setText(name);
                numberValueLabel.setText(number);
                amountValueLabel.setText(amount+"");
                loadTableData();
                clearUI();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid amount!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database error!", "Error", JOptionPane.ERROR_MESSAGE);
        }        
    }//GEN-LAST:event_saveButtonActionPerformed


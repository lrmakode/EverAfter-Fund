    private void loadTableData() {
        DefaultTableModel model = (DefaultTableModel) dataTable.getModel();
        model.setRowCount(0); // Clear existing rows

        try (Connection conn = DriverManager.getConnection("jdbc:sqlite:data.db"); Statement stmt = conn.createStatement(); ResultSet rs = stmt.executeQuery("SELECT id, name, number, village, mode, amount FROM entries ORDER BY id DESC")) {
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("name"),
                    rs.getString("number"),
                    rs.getString("village"),
                    rs.getString("mode"),
                    rs.getInt("amount"),
                });
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        applyAlternateRowColors();
    }

